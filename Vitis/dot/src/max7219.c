/* ── MAX7219 전용 딜레이 ─────────────────────────────────────── */
void short_delay(void)
{
    volatile int i;
    for (i = 0; i < 5000; i++) ;
}

/* ── GPIO 제어 ───────────────────────────────────────────────── */
void gpio_init_output(void)
{
    Xil_Out32(DIN_GPIO_ID  + GPIO_TRI_OFFSET, 0x0);
    Xil_Out32(LCLK_GPIO_ID + GPIO_TRI_OFFSET, 0x0);
    Xil_Out32(CS_GPIO_ID   + GPIO_TRI_OFFSET, 0x0);

    set_cs(1);
    set_din(0);
    set_lclk(0);
}

void set_din(int value)
{
    Xil_Out32(DIN_GPIO_ID + GPIO_DATA_OFFSET, value ? 1 : 0);
}

void set_lclk(int value)
{
    Xil_Out32(LCLK_GPIO_ID + GPIO_DATA_OFFSET, value ? 1 : 0);
}

void set_cs(int value)
{
    Xil_Out32(CS_GPIO_ID + GPIO_DATA_OFFSET, value ? 1 : 0);
}

/* ── MAX7219 ─────────────────────────────────────────────────── */
void max7219_send16(u8 addr, u8 data)
{
    u16 packet = ((u16)addr << 8) | data;
    int i;

    set_cs(0);
    for (i = 15; i >= 0; i--) {
        set_lclk(0);
        set_din((packet >> i) & 0x1);
        short_delay();
        set_lclk(1);
        short_delay();
    }
    set_lclk(0);
    set_cs(1);
    short_delay();
}

void max7219_init(void)
{
    max7219_send16(0x0F, 0x00); /* display test OFF      */
    max7219_send16(0x09, 0x00); /* decode mode: no decode */
    max7219_send16(0x0B, 0x07); /* scan limit: 8 digits  */
    max7219_send16(0x0A, 0x04); /* intensity             */
    max7219_send16(0x0C, 0x01); /* shutdown: normal op   */
}

void max7219_clear(void)
{
    int i;
    for (i = 1; i <= 8; i++)
        max7219_send16(i, 0x00);
}

void max7219_fill(void)
{
    int i;
    for (i = 1; i <= 8; i++)
        max7219_send16(i, 0xFF);
}

void max7219_display_pattern(const u8 pattern[8])
{
    int i;
    for (i = 0; i < 8; i++)
        max7219_send16(i + 1, pattern[i]);
}